<h1>Noticias de México</h1>

<p>Noticias México, tiene como objectivo dar a conocer las últimas noticias que los diferentes
	medios de comunicación publican en México. La intención es tenerte informado de los acontecimientos
	que suceden en nuestro país a través de una plataforma rápida y amigable.</p>

<p>
	La plataforma es gratuita y la intención es facilitar el acceso a la información sobre las noticias del momento.
</p>

<p>
	Para conocer la fuente de las noticias publicadas en esta plataforma, da click en el link de "noticia extraida de [fuente]".
</p>

<p>
	Gracias por visitar la página y mantenerte informado de las últimas noticias de México.
</p>

